#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	_nb;
	char	c;

	_nb = n;
	if (_nb < 0)
	{
		write(fd, "-", 1);
		_nb = -_nb;
	}
	if (_nb / 10)
	{
		ft_putnbr_fd(_nb / 10, fd);
		c = _nb % 10 + '0';
		write(fd, &c, 1);
	}
	else
	{
		c = _nb % 10 + '0';
		write(fd, &c, 1);
	}
}
